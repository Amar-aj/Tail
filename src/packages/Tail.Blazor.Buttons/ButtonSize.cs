namespace Tail.Blazor.Buttons;

/// <summary>
/// Represents the size of a button.
/// </summary>
public enum ButtonSize
{
    /// <summary>
    /// Extra small button size.
    /// </summary>
    Xs,
    
    /// <summary>
    /// Small button size.
    /// </summary>
    Sm,
    
    /// <summary>
    /// Medium button size (default).
    /// </summary>
    Md,
    
    /// <summary>
    /// Large button size.
    /// </summary>
    Lg,
    
    /// <summary>
    /// Extra large button size.
    /// </summary>
    Xl
}

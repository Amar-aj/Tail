namespace Tail.Blazor.Buttons;

/// <summary>
/// Represents the visual style variant of a button.
/// </summary>
public enum ButtonVariant
{
    /// <summary>
    /// Primary button style (default blue).
    /// </summary>
    Primary,
    
    /// <summary>
    /// Success button style (green).
    /// </summary>
    Success,
    
    /// <summary>
    /// Warning button style (yellow).
    /// </summary>
    Warning,
    
    /// <summary>
    /// Danger button style (red).
    /// </summary>
    Danger,
    
    /// <summary>
    /// Info button style (cyan).
    /// </summary>
    Info,
    
    /// <summary>
    /// Outline button style (border only).
    /// </summary>
    Outline,
    
    /// <summary>
    /// Soft button style (light background).
    /// </summary>
    Soft,
    
    /// <summary>
    /// Ghost button style (transparent background).
    /// </summary>
    Ghost,
    
    /// <summary>
    /// Link button style (text only).
    /// </summary>
    Link
}
